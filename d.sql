id INT NOT NULL PRIMARY KEY,
last_name varchar(255),
name varchar(255),
middle_name varchar(255),
job_id INT,
cathedra_id INT,
cval_id INT,
uch_stepen varchar(255),
uch_zvanie varchar(255),
all_stash INT,
spec_stash INT,
code varchar(255),
disciplines varchar(255),
FOREIGN KEY (job_id) REFERENCES job(id),
FOREIGN KEY (cathedra_id) REFERENCES cathedra(id),
FOREIGN KEY (cval_id) REFERENCES cval(id)
);

